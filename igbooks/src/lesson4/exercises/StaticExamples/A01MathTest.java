package lesson4.exercises.StaticExamples;
import static java.lang.Math.*;
public class A01MathTest {
    public static void main(String[] args) {
        System.out.println("Random: " + random() * 10);
        System.out.println("Square root: " + sqrt(9.0));
        System.out.println("Rounded random: " + round(random()*100));
        System.out.println("Abs: " + abs(-9));
    }
}
